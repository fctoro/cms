const buildFixture = (
  id: string,
  competition: string,
  round: string,
  kickoff: string,
  status: ClubFixture["status"],
  homeTeamId: keyof typeof clubCatalog,
  awayTeamId: keyof typeof clubCatalog,
  homeScore?: number,
  awayScore?: number,
): ClubFixture => ({
  id,
  competition,
  round,
  kickoff,
  status,
  homeTeamId,
  awayTeamId,
  homeTeamName: clubCatalog[homeTeamId].name,
  awayTeamName: clubCatalog[awayTeamId].name,
  homeLogoUrl: clubCatalog[homeTeamId].logoUrl,
  awayLogoUrl: clubCatalog[awayTeamId].logoUrl,
  homeScore,
  awayScore,
});

