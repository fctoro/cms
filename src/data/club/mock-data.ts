const paymentMethods: PaymentMethod[] = ["virement", "carte", "especes", "mobile"];

const paidDateForPeriod = (period: string, index: number) => {
  const day = String((index % 18) + 1).padStart(2, "0");
  return `${period}-${day}`;
};

const previousMonthStatus = (index: number): PaymentStatus =>
  index % 7 === 0 ? "late" : "paid";

export const mockPayments: Payment[] = mockPlayers.flatMap((player, index) => {
  const method = paymentMethods[index % paymentMethods.length];
  const currentPeriod = "2026-02";
  const previousPeriod = "2026-01";
  const olderPeriod = index % 2 === 0 ? "2025-12" : null;

  const currentPayment: Payment = {
    id: `pay-${player.id}-${currentPeriod}`,
    playerId: player.id,
    montant: player.cotisationMontant,
    statut: player.cotisationStatut,
    periode: currentPeriod,
    methode: method,
    datePaiement:
      player.cotisationStatut === "paid"
        ? player.dernierPaiement
        : undefined,
  };

  const previousPayment: Payment = {
    id: `pay-${player.id}-${previousPeriod}`,
    playerId: player.id,
    montant: player.cotisationMontant,
    statut: previousMonthStatus(index),
    periode: previousPeriod,
    methode: method,
    datePaiement: paidDateForPeriod(previousPeriod, index),
  };

  const payments: Payment[] = [currentPayment, previousPayment];

  if (olderPeriod) {
    payments.push({
      id: `pay-${player.id}-${olderPeriod}`,
      playerId: player.id,
      montant: player.cotisationMontant,
      statut: "paid",
      periode: olderPeriod,
      methode: method,
      datePaiement: paidDateForPeriod(olderPeriod, index + 3),
    });
  }

  return payments;
});

